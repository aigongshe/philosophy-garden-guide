export function getPopularPosts(limit: number = 10): Post[] {
  return posts
    .sort((a, b) => b.popularity - a.popularity)
    .slice(0, limit);
}

// 工具函数：搜索文章
export function searchPosts(query: string): Post[] {
  const lowercaseQuery = query.toLowerCase();
  return posts.filter(post => 
    post.title.toLowerCase().includes(lowercaseQuery) ||
    post.description.toLowerCase().includes(lowercaseQuery) ||
    post.excerpt.toLowerCase().includes(lowercaseQuery) ||
    post.tags.some(tag => tag.toLowerCase().includes(lowercaseQuery))
  );
}

// 工具函数：获取相关文章
export function getRelatedPosts(currentPost: Post, limit: number = 3): Post[] {
  return posts
    .filter(post => 
      post.slug !== currentPost.slug && 
      (post.categorySlug === currentPost.categorySlug || 
       post.tags.some(tag => currentPost.tags.includes(tag)))
    )
    .sort((a, b) => b.popularity - a.popularity)
    .slice(0, limit);
}

// 工具函数：获取文章统计
export function getPostStats() {
  const total = posts.length;
  const published = posts.filter(p => p.status === 'published').length;
  const featured = posts.filter(p => p.featured).length;
  const avgReadingTime = Math.round(posts.reduce((sum, p) => sum + p.readingTime, 0) / total);
  const avgPopularity = Math.round(posts.reduce((sum, p) => sum + p.popularity, 0) / total);

  const byCategory = posts.reduce((acc, post) => {
    acc[post.categorySlug] = (acc[post.categorySlug] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  const byDifficulty = posts.reduce((acc, post) => {
    acc[post.difficulty] = (acc[post.difficulty] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  return {
    total,
    published,
    featured,
    avgReadingTime,
    avgPopularity,
    byCategory,
    byDifficulty
  };
}

