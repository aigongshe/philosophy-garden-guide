  const total = posts.length;
  const published = posts.filter(p => p.status === 'published').length;
  const featured = posts.filter(p => p.featured).length;
  const avgReadingTime = Math.round(posts.reduce((sum, p) => sum + p.readingTime, 0) / total);
  const avgPopularity = Math.round(posts.reduce((sum, p) => sum + p.popularity, 0) / total);

  const byCategory = posts.reduce((acc, post) => {
    acc[post.categorySlug] = (acc[post.categorySlug] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  const byDifficulty = posts.reduce((acc, post) => {
    acc[post.difficulty] = (acc[post.difficulty] || 0) + 1;
    return acc;
  }, {} as Record<string, number>);

  return {
    total,
    published,
    featured,
    avgReadingTime,
    avgPopularity,
    byCategory,
    byDifficulty
  };
